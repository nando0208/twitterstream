import UIKit

// Reference colors: https://www.lockedowndesign.com/social-media-colors/#twitter

extension UIColor {
    static var blueTwitter: UIColor {
        return UIColor(red: 29.0 / 255.0, green: 161.0 / 255.0, blue: 242.0 / 255.0, alpha: 1.0)
    }

    static var blackTwitter: UIColor {
        return UIColor(red: 20.0 / 255.0, green: 23.0 / 255.0, blue: 26.0 / 255.0, alpha: 1.0)
    }

    static var darkGrayTwitter: UIColor {
        return UIColor(red: 170.0 / 255.0, green: 184.0 / 255.0, blue: 194.0 / 255.0, alpha: 1.0)
    }

    static var lightGrayTwitter: UIColor {
        return UIColor(red: 255.0 / 255.0, green: 232.0 / 255.0, blue: 237.0 / 255.0, alpha: 1.0)
    }

    static var whiteTwitter: UIColor {
        return UIColor(red: 245.0 / 255.0, green: 248.0 / 255.0, blue: 240.0 / 255.0, alpha: 1.0)
    }
}
